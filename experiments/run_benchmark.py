if __name__ == "__main__":

    import argparse
    import gc
    import json
    import time
    from pathlib import Path

    import pandas as pd
    import pyarrow as pa
    import pyarrow.compute as pc

    from thor.analysis import ObservationLabels, analyze_orbit
    from thor.config import Config
    from thor.main import link_test_orbits
    from thor.observations import Observations
    from thor.orbit import TestOrbits

    # ── Object catalog (must match generate_benchmark.py) ─────────────
    OBJECT_CATALOG = {
        "2020 AV2": "Atira",
        "163693": "Atira",
        "2010 TK7": "Aten",
        "3753": "Aten",
        "54509": "Apollo",
        "2063": "Apollo",
        "1221": "Amor",
        "433": "Amor",
        "3908": "Amor",
        "1876": "Inner Main Belt",
        "2001": "Inner Main Belt",
        "6522": "Main Belt",
        "10297": "Main Belt",
        "17032": "Main Belt",
        "202930": "Main Belt",
        "911": "Jupiter Trojan",
        "1143": "Jupiter Trojan",
        "1172": "Jupiter Trojan",
        "3317": "Jupiter Trojan",
        "5145": "Centaur",
        "5335": "Centaur",
        "15760": "Trans-Neptunian Object",
        "15788": "Trans-Neptunian Object",
        "15789": "Trans-Neptunian Object",
        "A/2017 U1": "Interstellar Object",
    }

    CADENCE_MAP = {
        "singletons": 1,
        "pairs": 2,
        "triplets": 3,
        "quads": 4,
    }

    def safe_filename(object_id):
        """Sanitize object ID for use as a filename/directory name."""
        return object_id.replace("/", "_").replace(" ", "_")

    # ── Argument parsing ──────────────────────────────────────────────
    parser = argparse.ArgumentParser(
        description="Run clustering pipeline on benchmark datasets generated by "
        "generate_benchmark.py. Produces summary.parquet with per-sample metrics.",
    )
    parser.add_argument(
        "--benchmark_dir", type=str, default="experiments/benchmark",
        help="Path to benchmark directory created by generate_benchmark.py.",
    )
    parser.add_argument("--max_processes", type=int, default=24)
    parser.add_argument(
        "--object_ids",
        type=str,
        nargs="+",
        default=None,
        help="Run only these object IDs (default: all in benchmark).",
    )
    parser.add_argument(
        "--dynamical_classes",
        type=str,
        nargs="+",
        default=None,
        help="Run only objects in these dynamical classes.",
    )
    parser.add_argument(
        "--linking_windows",
        type=int,
        nargs="+",
        default=None,
        help="Run only these linking windows (default: all in benchmark).",
    )
    parser.add_argument(
        "--cadences",
        type=str,
        nargs="+",
        default=None,
        help="Run only these cadences (default: all in benchmark).",
    )
    parser.add_argument(
        "--noise_densities",
        type=int,
        nargs="+",
        default=None,
        help="Run only these noise densities (default: all in benchmark).",
    )
    parser.add_argument(
        "--cluster_algorithm",
        type=str,
        default="dbscan",
        help="Clustering algorithm to use (default: dbscan). "
        "Options: dbscan, hotspot_2d, optics, kdtree, fft, hough.",
    )
    args = parser.parse_args()

    benchmark_dir = Path(args.benchmark_dir)
    data_dir = benchmark_dir / "data"
    orbits_dir = benchmark_dir / "orbits"
    results_dir = benchmark_dir / "results" / args.cluster_algorithm
    results_dir.mkdir(parents=True, exist_ok=True)

    # ── Load experiment config ────────────────────────────────────────
    config_path = benchmark_dir / "experiment_config.json"
    if not config_path.exists():
        parser.error(
            f"experiment_config.json not found in {benchmark_dir}. "
            "Run generate_benchmark.py first."
        )

    with open(config_path) as f:
        experiment_config = json.load(f)

    # Determine what to run (intersection of benchmark data and CLI filters)
    all_object_ids = experiment_config["object_ids"]
    all_windows = experiment_config["linking_windows"]
    all_cadences = experiment_config["cadences"]
    all_noise_densities = experiment_config["noise_densities"]

    if args.object_ids is not None:
        run_object_ids = [oid for oid in args.object_ids if oid in all_object_ids]
    elif args.dynamical_classes is not None:
        run_object_ids = [
            oid for oid in all_object_ids
            if OBJECT_CATALOG.get(oid) in args.dynamical_classes
        ]
    else:
        run_object_ids = all_object_ids

    run_windows = args.linking_windows if args.linking_windows is not None else all_windows
    run_cadences = args.cadences if args.cadences is not None else all_cadences
    run_noise = args.noise_densities if args.noise_densities is not None else all_noise_densities

    # ── THOR pipeline configuration ───────────────────────────────────
    config = Config(
        max_processes=args.max_processes,
        ray_memory_bytes=0,
        propagator_namespace="adam_assist.ASSISTPropagator",
        filter_cell_radius=None,
        filter_mahalanobis_distance=1.0,
        filter_chunk_size=500_000,
        cluster_vx_min=-0.1,
        cluster_vx_max=0.1,
        cluster_vy_min=-0.1,
        cluster_vy_max=0.1,
        cluster_vx_bins=300,
        cluster_vy_bins=300,
        cluster_radius=0.005,
        cluster_mahalanobis_distance=None,
        cluster_velocity_bin_separation=2.0,
        cluster_whiten=False,
        cluster_min_obs=6,
        cluster_min_arc_length=1.0,
        cluster_min_nights=3,
        cluster_rchi2_threshold=1e3,
        cluster_algorithm=args.cluster_algorithm,
        cluster_chunk_size=500,
        split_threshold=None,
        split_max_depth=2,
        split_method="eigenvalue",
        iod_min_obs=6,
        iod_min_arc_length=1.0,
        iod_contamination_percentage=20.0,
        iod_rchi2_threshold=1e3,
        iod_observation_selection_method="combinations",
        iod_chunk_size=10,
        od_min_obs=6,
        od_min_arc_length=1.0,
        od_contamination_percentage=20.0,
        od_rchi2_threshold=1e2,
        od_delta=1e-6,
        od_max_iter=10,
        od_chunk_size=10,
        arc_extension_min_obs=6,
        arc_extension_min_arc_length=1.0,
        arc_extension_contamination_percentage=0.0,
        arc_extension_rchi2_threshold=10,
        arc_extension_radius=1 / 3600,
        arc_extension_chunk_size=100,
        stop_after_stage="cluster_and_link",
    )

    # Save config alongside results
    config_out = results_dir / "config.json"
    with open(config_out, "w") as f:
        f.write(config.json(indent=2))

    # ── Load existing summary to skip completed cases ─────────────────
    summary_path = benchmark_dir / f"summary_{args.cluster_algorithm}.parquet"
    completed_keys = set()
    if summary_path.exists():
        existing_df = pd.read_parquet(summary_path)
        for _, row in existing_df.iterrows():
            completed_keys.add((
                row["object_id"],
                int(row["linking_window"]),
                row["cadence"],
                int(row["noise_density"]),
            ))
        print(f"Found {len(completed_keys)} already-completed cases in summary.")
    else:
        existing_df = None

    n_total_cases = len(run_object_ids) * len(run_windows) * len(run_cadences) * len(run_noise)
    print("=" * 70)
    print(f"Clustering Benchmark Runner — {args.cluster_algorithm}")
    print("=" * 70)
    print(f"  Benchmark dir    : {benchmark_dir}")
    print(f"  Algorithm        : {args.cluster_algorithm}")
    print(f"  Objects          : {len(run_object_ids)}")
    print(f"  Linking windows  : {run_windows}")
    print(f"  Cadences         : {run_cadences}")
    print(f"  Noise densities  : {run_noise}")
    print(f"  Total cases      : {n_total_cases}")
    print("=" * 70)

    # ── Main loop ─────────────────────────────────────────────────────
    all_results = []
    case_counter = 0
    skipped = 0

    from adam_core.orbits.orbits import Orbits

    for obj_idx, object_id in enumerate(run_object_ids):
        dynamical_class = OBJECT_CATALOG.get(object_id, "Unknown")

        # Load orbit
        orbit_path = orbits_dir / f"{safe_filename(object_id)}.parquet"
        if not orbit_path.exists():
            print(f"  WARNING: orbit not found for {object_id}, skipping.")
            continue
        orbit = Orbits.from_parquet(str(orbit_path))
        test_orbit = TestOrbits.from_orbits(orbit)
        test_orbit_id = test_orbit.orbit_id[0].as_py()

        # Load sample orbits
        sample_orbits_path = data_dir / safe_filename(object_id) / "sample_orbits.parquet"
        if not sample_orbits_path.exists():
            print(f"  WARNING: sample orbits not found for {object_id}, skipping.")
            continue
        sample_orbits = Orbits.from_parquet(str(sample_orbits_path))

        print(f"\n{'=' * 70}")
        print(f"[Object {obj_idx + 1}/{len(run_object_ids)}] "
              f"{object_id} ({dynamical_class})")
        print(f"{'=' * 70}")

        for n_nights in run_windows:
            for cadence_name in run_cadences:
                combo_label = f"w{n_nights:03d}_{cadence_name}"

                for noise_density in run_noise:
                    case_counter += 1
                    case_key = (object_id, n_nights, cadence_name, noise_density)

                    # Skip if already completed
                    if case_key in completed_keys:
                        skipped += 1
                        continue

                    noise_label = f"noise_{noise_density:06d}"
                    safe_oid = safe_filename(object_id)
                    obs_path = data_dir / safe_oid / combo_label / noise_label / "observations.parquet"
                    labels_path = data_dir / safe_oid / combo_label / noise_label / "labels.parquet"

                    if not obs_path.exists() or not labels_path.exists():
                        print(f"  [{combo_label}/{noise_label}] Data not found, skipping.")
                        continue

                    print(f"\n  [{combo_label}/{noise_label}] ", end="")

                    # Load data
                    observations = Observations.from_parquet(str(obs_path))
                    labels = ObservationLabels.from_parquet(str(labels_path))
                    n_total = len(observations)
                    print(f"{n_total} observations")

                    # Set up output directory for pipeline
                    run_dir = results_dir / safe_oid / combo_label / noise_label
                    run_dir.mkdir(parents=True, exist_ok=True)

                    # ── Run pipeline ──────────────────────────────
                    t_start = time.time()
                    try:
                        for result in link_test_orbits(
                            test_orbit,
                            observations,
                            config=config,
                            working_dir=str(run_dir),
                            yield_paths=True,
                        ):
                            print(f"    Stage: {result.name}")
                    except Exception as e:
                        print(f"    Pipeline error: {e}")
                    t_elapsed = time.time() - t_start

                    # ── Analysis ──────────────────────────────────
                    orbit_output_dir = run_dir / test_orbit_id

                    # Count signal/noise from labels
                    n_signal = pc.sum(pc.is_valid(labels.object_id)).as_py()
                    n_noise_obs = n_total - n_signal
                    n_exposures = len(observations.exposure_id.unique())

                    try:
                        all_objects, partition_summary = analyze_orbit(
                            orbit_dir=orbit_output_dir,
                            config=config,
                            labels=labels,
                            max_processes=1,
                            out_dir=str(run_dir / "analysis"),
                        )

                        for sample_idx in range(len(sample_orbits)):
                            sample_orbit_id = sample_orbits[sample_idx].orbit_id[0].as_py()

                            run_result = {
                                "cluster_algorithm": args.cluster_algorithm,
                                "object_id": object_id,
                                "dynamical_class": dynamical_class,
                                "linking_window": n_nights,
                                "cadence": cadence_name,
                                "noise_density": noise_density,
                                "sample_orbit_id": sample_orbit_id,
                                "n_exposures": n_exposures,
                                "n_signal_total": n_signal,
                                "n_noise_total": n_noise_obs,
                                "n_total": n_total,
                                "pipeline_elapsed_s": t_elapsed,
                            }

                            if len(all_objects) > 0:
                                obj = all_objects.apply_mask(
                                    pc.equal(
                                        all_objects.object_id,
                                        pa.scalar(sample_orbit_id, pa.large_string()),
                                    )
                                )
                                if len(obj) > 0:
                                    run_result["findable"] = obj.findable[0].as_py()
                                    run_result["found_pure_clusters"] = obj.found_pure_clusters[0].as_py()
                                    run_result["found_contaminated_clusters"] = obj.found_contaminated_clusters[0].as_py()
                                    run_result["pure_clusters"] = obj.pure_clusters[0].as_py()
                                    run_result["pure_complete_clusters"] = obj.pure_complete_clusters[0].as_py()
                                    run_result["contaminated_clusters"] = obj.contaminated_clusters[0].as_py()
                                    run_result["obs_in_pure_clusters"] = obj.obs_in_pure_clusters[0].as_py()
                                    run_result["obs_in_pure_complete_clusters"] = obj.obs_in_pure_complete_clusters[0].as_py()

                            all_results.append(run_result)

                        if len(partition_summary) > 0:
                            completeness = partition_summary.completeness_clusters[0].as_py()
                            total_found = partition_summary.found_clusters[0].as_py()
                            print(f"    Analysis: found={total_found}, "
                                  f"completeness={completeness:.3f}, "
                                  f"elapsed={t_elapsed:.1f}s")
                        else:
                            print(f"    Analysis: no partition summary, "
                                  f"elapsed={t_elapsed:.1f}s")

                    except FileNotFoundError:
                        print(f"    Analysis: skipped (filtered_observations not found), "
                              f"elapsed={t_elapsed:.1f}s")
                        for sample_idx in range(len(sample_orbits)):
                            all_results.append({
                                "cluster_algorithm": args.cluster_algorithm,
                                "object_id": object_id,
                                "dynamical_class": dynamical_class,
                                "linking_window": n_nights,
                                "cadence": cadence_name,
                                "noise_density": noise_density,
                                "sample_orbit_id": sample_orbits[sample_idx].orbit_id[0].as_py(),
                                "n_exposures": n_exposures,
                                "n_signal_total": n_signal,
                                "n_noise_total": n_noise_obs,
                                "n_total": n_total,
                                "pipeline_elapsed_s": t_elapsed,
                                "error": "filtered_observations_not_found",
                            })

                    except Exception as e:
                        print(f"    Analysis: failed ({e}), elapsed={t_elapsed:.1f}s")
                        for sample_idx in range(len(sample_orbits)):
                            all_results.append({
                                "cluster_algorithm": args.cluster_algorithm,
                                "object_id": object_id,
                                "dynamical_class": dynamical_class,
                                "linking_window": n_nights,
                                "cadence": cadence_name,
                                "noise_density": noise_density,
                                "sample_orbit_id": sample_orbits[sample_idx].orbit_id[0].as_py(),
                                "n_exposures": n_exposures,
                                "n_signal_total": n_signal,
                                "n_noise_total": n_noise_obs,
                                "n_total": n_total,
                                "pipeline_elapsed_s": t_elapsed,
                                "error": str(e),
                            })

                    del observations
                    gc.collect()

        del sample_orbits
        gc.collect()

    # ── Save summary ──────────────────────────────────────────────────
    print("\n[Summary] Saving results...")

    new_df = pd.DataFrame(all_results)

    # Merge with existing results
    if existing_df is not None and len(existing_df) > 0:
        summary_df = pd.concat([existing_df, new_df], ignore_index=True)
    else:
        summary_df = new_df

    if len(summary_df) > 0:
        summary_df.to_parquet(str(summary_path), index=False)
        summary_df.to_csv(str(benchmark_dir / f"summary_{args.cluster_algorithm}.csv"), index=False)

        print(f"  Summary: {len(summary_df)} rows "
              f"({len(new_df)} new, {skipped} skipped)")
        print(f"  Saved to: {summary_path}")

        if "found_pure_clusters" in summary_df.columns:
            print("\n  ── Results by (linking_window, cadence, noise_density) ──")
            agg = summary_df.groupby(
                ["linking_window", "cadence", "noise_density"]
            ).agg({
                "found_pure_clusters": ["mean", "sum"],
                "findable": "sum",
            })
            print(agg.to_string())
    else:
        print("  No results to save.")

    print("\n" + "=" * 70)
    print("Benchmark run complete.")
    print(f"Results: {benchmark_dir}")
    print("=" * 70)
